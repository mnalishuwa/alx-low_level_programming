#include <unistd.h>

/* _putchar - takes char c and outputs to stdout */
int _putchar(char c);

/* _isupper - takes char c and checks if c is uppercase */
int _isupper(int c);

/* _isdigit - takes char c and checks if c is digit 0 through 9 */
int _isdigit(int c);

/* mul - takes two ints and returns the product */
int mul(int a, int b);
