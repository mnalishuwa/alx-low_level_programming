#include <unistd.h>

/* _putchar - takes char c and outputs to stdout */
int _putchar(char c);

/* _isupper - takes char c and checks if c is uppercase */
int _isupper(int c);

/* _isdigit - takes char c and checks if c is digit 0 through 9 */
int _isdigit(int c);

/* mul - takes two ints and returns the product */
int mul(int a, int b);

/* print_numbers - prints numbers 0 through 9 */
void print_numbers(void);

/* print_most_numbers - prints numbers 0 through 9 except 2 and 4 */
void print_most_numbers(void);

/* more_numbers - print numbers 0 through 14, 10 times */
void more_numbers(void);

/* print_line - print as line using the underscore character */
void print_line(int n);

/* print_diagonal - print a diagonal line using the backslash character */
void print_diagonal(int n);

/* print_square - print a square using # character */
void print_square(int size);

/* print_triangle - print triangle using # character */
void print_triangle(int size);
