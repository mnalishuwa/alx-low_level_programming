#include "3-calc.h"

/**
 * op_add - returns the sum of two integers
 * Description: receives 2 ints and returns a + b
 *
 * @a: int, first arg
 * @b: int, second arg
 *
 * Return: int, (a + b)
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of two integers
 * Description: receives 2 ints and returns a - b
 *
 * @a: int, first arg
 * @b: int, second arg
 *
 * Return: int, (a - b)
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of two integers
 * Description: receives 2 ints and returns a * b
 *
 * @a: int, first arg
 * @b: int, second arg
 *
 * Return: int, (a * b)
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns integer division of two integers
 * Description: receives 2 ints and returns a divide by b
 *
 * @a: int, first arg
 * @b: int, second arg
 *
 * Return: int, (a /  b)
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of division of two integers
 * Description: receives 2 ints and returns a modulo b
 *
 * @a: int, first arg
 * @b: int, second arg
 *
 * Return: int, (a % b)
 */

int op_mod(int a, int b)
{
	return (a % b);
}
