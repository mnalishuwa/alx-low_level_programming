#include <unistd.h>

/* putchar - writes a single char to stdout */
int _putchar(char c);

/* reset_to_98 - updates the value stored in a pointer */
void reset_to_98(int *n);

/* swap - swaps the value of two integers */
void swap_int(int *a, int *b);

/* _strlen - returns the length of a string */
int _strlen(char *s);

/* _puts - prints a string to stdout */
void _puts(char *str);

/* print_rev - prints a string in reverse */
void print_rev(char *s);

/* rev_tring - reverses a string */
void rev_string(char *s);

/* puts2 - prints every other character in a string */
void puts2(char *str);

/* puts_half - prints half of a string */
void puts_half(char *str);

/* print_array - print elements of an array of numbers */
void print_array(int *a, int n);

/* *_strcpy - on string to a memory location */
char *_strcpy(char *dest, char *src);
